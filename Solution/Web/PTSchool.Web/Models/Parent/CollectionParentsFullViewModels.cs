using PTSchool.Services.Models.Parent;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace PTSchool.Web.Models.Parent
{
    public class CollectionParentsFullViewModels
    {
        public IEnumerable<ParentFullServiceModel> AllParentsFull { get; set; }


        public int CurrentPage { get; set; }

        public int FirstPage => 1;

        public int LastPage
        {
            get
            {
                return (int)Math.Ceiling((double)this.Total / 25);
            }
        }


        public int PreviousPage => this.CurrentPage - 1;

        public int NextPage => this.CurrentPage + 1;


        public bool PreviousDisabled => this.CurrentPage == 1;

        public bool NextDisabled
        {
            get
            {
                var maxPage = Math.Ceiling((double)this.Total / 25);

                return maxPage == this.CurrentPage;
            }
        }


        public int Total { get; set; }        
    }
}
